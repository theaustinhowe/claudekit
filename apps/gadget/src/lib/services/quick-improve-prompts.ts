interface PersonaConfig {
  label: string;
  branchPrefix: string;
  commitMessage: string;
  prTitle: (repoName: string) => string;
  prBody: (repoName: string, claudeOutput: string) => string;
  buildPrompt: (repoPath: string) => string;
  allowedTools: string;
}

function buildEnvironmentContext(repoPath: string): string {
  return `## Environment
You are working inside a git worktree — an isolated copy of the repository at: ${repoPath}
This is your working directory. Treat it as the project root.

Important constraints:
- The Bash tool is NOT available. Do not attempt to run shell commands.
- Focus exclusively on reading, analyzing, and editing files using the tools available to you (Read, Write, Edit, Glob, Grep).
- Do NOT attempt any git operations (commit, push, diff, status, etc.) — the system handles all git workflow automatically after you finish.
- Do NOT try to verify your changes via git. Trust that the edits you make are persisted.
- When you are done making changes, provide your summary and stop.

`;
}

